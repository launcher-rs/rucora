//! AgentKit 研究助手示例
//!
//! 综合示例：结合 Provider、Tools 等模块，创建一个智能研究助手。
//!
//! ## 运行方法
//! ```bash
//! export OPENAI_API_KEY=sk-your-key
//! cargo run --example 18_research_assistant
//! ```
//!
//! ## 功能演示
//!
//! 1. **信息搜集** - 使用工具搜集相关资料
//! 2. **资料整理** - 整理和分类信息
//! 3. **分析总结** - 分析资料并生成总结
//! 4. **报告生成** - 生成结构化研究报告

use agentkit::agent::ToolAgent;
use agentkit::prelude::Agent;
use agentkit::provider::OpenAiProvider;
use agentkit::tools::{EchoTool, ShellTool};
use tracing::{Level, info};
use tracing_subscriber::FmtSubscriber;

#[tokio::main]
async fn main() -> anyhow::Result<()> {
    dotenv::dotenv().ok();

    // 初始化日志
    let subscriber = FmtSubscriber::builder()
        .with_max_level(Level::INFO)
        .with_target(false)
        .finish();
    tracing::subscriber::set_global_default(subscriber)?;

    info!("╔════════════════════════════════════════╗");
    info!("║   AgentKit 研究助手示例               ║");
    info!("╚════════════════════════════════════════╝\n");

    // 检查配置
    if std::env::var("OPENAI_API_KEY").is_err() && std::env::var("OPENAI_BASE_URL").is_err() {
        info!("⚠ 未设置 API 配置");
        info!("   使用 OpenAI: export OPENAI_API_KEY=sk-your-key");
        info!("   使用 Ollama: export OPENAI_BASE_URL=http://localhost:11434");
        return Ok(());
    }

    let model_name = std::env::var("MODEL_NAME").unwrap_or_else(|_| "gpt-4o-mini".to_string());

    // ═══════════════════════════════════════════════════════════
    // 创建研究助手
    // ═══════════════════════════════════════════════════════════
    info!("═══════════════════════════════════════");
    info!("创建研究助手");
    info!("═══════════════════════════════════════\n");

    info!("1. 创建 Provider...");
    let provider = OpenAiProvider::from_env()?;
    info!("✓ Provider 创建成功\n");

    info!("2. 创建研究助手 Agent...");
    let agent = ToolAgent::builder()
        .provider(provider)
        .model(&model_name)
        .system_prompt(
            "你是一个智能研究助手。你可以帮助用户：\n\
             - 搜集和整理信息\n\
             - 分析和总结资料\n\
             - 生成研究报告\n\n\
             请遵循以下研究流程：\n\
             1. 理解研究主题\n\
             2. 搜集相关资料\n\
             3. 整理和分类信息\n\
             4. 分析和综合\n\
             5. 生成结构化报告\n\n\
             请使用可用的工具完成任务。",
        )
        .tool(EchoTool)
        .tool(ShellTool)
        .max_steps(10)
        .build();
    info!("✓ 研究助手创建成功\n");

    // ═══════════════════════════════════════════════════════════
    // 研究助手功能
    // ═══════════════════════════════════════════════════════════
    info!("═══════════════════════════════════════");
    info!("研究助手功能:");
    info!("═══════════════════════════════════════");
    info!("1. 信息搜集 - 使用工具搜集相关资料");
    info!("2. 资料整理 - 整理和分类信息");
    info!("3. 分析总结 - 分析资料并生成总结");
    info!("4. 报告生成 - 生成结构化研究报告");
    info!("═══════════════════════════════════════\n");

    // ═══════════════════════════════════════════════════════════
    // 演示任务 1: 技术调研
    // ═══════════════════════════════════════════════════════════
    info!("═══════════════════════════════════════");
    info!("演示任务 1: 技术调研");
    info!("═══════════════════════════════════════\n");

    let task1 = "帮我调研 Rust 生态系统中常用的 Web 框架，列出主要框架及其特点";
    info!("任务：\"{}\"\n", task1);

    match agent.run(task1.into()).await {
        Ok(output) => {
            if let Some(text) = output.text() {
                info!("研究报告：\n{}\n", text);
            }
        }
        Err(e) => {
            info!("错误：{}\n", e);
        }
    }

    // ═══════════════════════════════════════════════════════════
    // 演示任务 2: 文献综述
    // ═══════════════════════════════════════════════════════════
    info!("═══════════════════════════════════════");
    info!("演示任务 2: 文献综述");
    info!("═══════════════════════════════════════\n");

    let task2 = "请总结人工智能在医疗领域的主要应用场景";
    info!("任务：\"{}\"\n", task2);

    match agent.run(task2.into()).await {
        Ok(output) => {
            if let Some(text) = output.text() {
                info!("综述报告：\n{}\n", text);
            }
        }
        Err(e) => {
            info!("错误：{}\n", e);
        }
    }

    // ═══════════════════════════════════════════════════════════
    // 演示任务 3: 竞品分析
    // ═══════════════════════════════════════════════════════════
    info!("═══════════════════════════════════════");
    info!("演示任务 3: 竞品分析");
    info!("═══════════════════════════════════════\n");

    let task3 = "分析主要的云服务商（AWS、Azure、GCP）的优缺点";
    info!("任务：\"{}\"\n", task3);

    match agent.run(task3.into()).await {
        Ok(output) => {
            if let Some(text) = output.text() {
                info!("分析报告：\n{}\n", text);
            }
        }
        Err(e) => {
            info!("错误：{}\n", e);
        }
    }

    // ═══════════════════════════════════════════════════════════
    // 研究方法论
    // ═══════════════════════════════════════════════════════════
    info!("═══════════════════════════════════════");
    info!("研究方法论:");
    info!("═══════════════════════════════════════\n");

    info!("1. 定义研究问题:");
    info!("   - 明确研究目标");
    info!("   - 确定研究范围");
    info!("   - 制定研究计划\n");

    info!("2. 搜集资料:");
    info!("   - 使用搜索引擎");
    info!("   - 查阅学术文献");
    info!("   - 参考官方文档\n");

    info!("3. 整理信息:");
    info!("   - 分类整理");
    info!("   - 提取关键点");
    info!("   - 建立知识框架\n");

    info!("4. 分析综合:");
    info!("   - 比较不同来源");
    info!("   - 识别模式和趋势");
    info!("   - 形成自己的见解\n");

    info!("5. 生成报告:");
    info!("   - 结构化组织");
    info!("   - 清晰表达");
    info!("   - 引用来源\n");

    // ═══════════════════════════════════════════════════════════
    // 总结
    // ═══════════════════════════════════════════════════════════
    info!("═══════════════════════════════════════");
    info!("示例完成！");
    info!("═══════════════════════════════════════\n");

    info!("📝 研究助手总结：\n");

    info!("1. 研究助手能力:");
    info!("   - 信息搜集 - 快速找到相关资料");
    info!("   - 资料整理 - 有序组织信息");
    info!("   - 分析综合 - 提炼关键见解");
    info!("   - 报告生成 - 输出结构化内容\n");

    info!("2. 适用场景:");
    info!("   - 技术调研 - 了解新技术");
    info!("   - 文献综述 - 总结研究领域");
    info!("   - 竞品分析 - 比较竞争对手");
    info!("   - 市场研究 - 分析市场趋势\n");

    info!("3. 增强建议:");
    info!("   - 集成 RAG - 检索专业文档");
    info!("   - 集成记忆 - 记住研究历史");
    info!("   - 集成工具 - 更多数据源");
    info!("   - 多 Agent 协作 - 专家分工\n");

    Ok(())
}
